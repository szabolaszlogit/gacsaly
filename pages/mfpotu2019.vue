A projekt célja Gacsály Szabadság és Táncsics utcáinak felújítása. Célunk a két legrosszabb minőségű út felújításával a közlekedési infrastruktúra fejlesztése, ezáltal az alapvető szolgáltatások hozzáférhetőségének javítása, a településkép épülése, ezzel elősegítve a lakosság életszínvonalának javítását. Mivel településünk lakói túlnyomóan mezőgazdasági jellegű tevékenységet folytatnak, nagy szűkség van az utak állandó járhatóságának biztosítására. Az önkormányzati utak karbantartását Gacsályban álláskeresők foglalkoztatásával, közfoglalkoztatás kapcsán oldjuk meg. <br><br>

A Szabadság utca felújításra szoruló hossza összesen 230 méter, felülete 690 négyzetméter. Átlagos szélessége 3 méter. Az út az elvégzett felmérés alapján rossz állapotú. A megvalósítandó technológiai során új aszfaltszőnyeg terítése történik meg. Az út két oldalán átlag 1 méter széles padkarendezés kerül betervezésre.<br><br>

A Táncsics utca felújításra szoruló hossza összesen 310 méter, felülete 992 négyzetméter. Átlagos szélessége 3,2 méter. Az út az elvégzett felmérés alapján rossz állapotú. A megvalósítandó technológiai során új aszfaltszőnyeg terítése történik meg. Az út két oldalán átlag 1 méter széles padkarendezés kerül betervezésre.
