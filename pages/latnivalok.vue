 A település református temploma a község középpontjában található, már az 1332-37. évi pápai tizedjegyzék is említi. Az 1717-es tatár betörés csaknem teljesen elpusztított, s mintegy 40 év multán újjáépítették. Jelen formáját akkor kapta meg. Az épület gótikus stílusú, aszimmetrikus, egy a tornyot áttörő fő, és egy oldalbejárata van. Az északi oldalon talált falmaradványok arról tanúskodnak, hogy korábban szimmetrikus volt. A templom szabadon álló, keletelt, egyhajós, homlokzat előtti tornyos, legutóbbi restaulása 1959-1960 történt tetőszék kicserélésével. A templom anyaga tégla, sajátossága a festett, kazettás famennyezet, mely 1759-ben készült, mely eredetileg 172 táblából állt. Mára sajnos csak 46 maradt fnt 24+16+6 tábla található, kék keretlécbe foglalva. Rajzuk változatos, népies motívumokat tartalmaz. A templom 36 méter magas, nyolcszögletű tornya két haranggal rendelkezik, 100 ill. 251 kg súlyúak. A kisebbiket 1793-ban öntötték Tasnádon, nagyobbikat 1928-ban Budapesten, felirata arról árulkodik, hogy korábban is hasonló harang állt helyén. A felirat szövege: „ A nagy Háborúból visszajöttem újra, zengő szám hirdeti, áldott az ég Ura!” A torony déli oldalán található Bárczy - kripta jelenleg hozzáférhetetlen.

A templom szomszédságában található az 1834-ben épített református paplak, mely egyszerű falusi, elkésett barokk épület. 

