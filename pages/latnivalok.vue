A község északkeleti részén található a Polgármesteri Hivatal, melynek a XX. Század elején épített „Madarassy - kuria” ad otthont. Az épület főbejárata eredetileg utcafrontra nyíló timpanonnal lezárt kiugró tornác volt, melyet négy sarkán mészkőoszlopok támasztottak alá, s hozzá faragott mészkő díszlépcsősor vezetett fel. Az épület mészkő lábazatú, tégla falazatú, a sarkait faragott mészkőből rakták. A 70-es években tornácot körbefalazták, a lépcsőt elbontották, így kialakítva egy nagyobb helyiséget, a jelenlegi dísztermet. Udvari oldalán még megtalálhatóak a dór stílusú mészkőoszlopok, mely a tornác teljes hosszában tartják a fedélszéket, egyedül az udvari bejárat került itt befalazásra. Az épület keleti végében szintén a 70-es években építettek lépcsőt, s az épület nyílászáróit is nagyobbakra cserélték.

